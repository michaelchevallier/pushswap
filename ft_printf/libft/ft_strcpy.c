#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	size_t		i;

	i = 0;
	while (i < (ft_strlen(src) + 1))
	{
		dst[i] = src[i];
		i++;
	}
	return (dst);
}
